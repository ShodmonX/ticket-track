def format_car_class(car_type: str, service_type: str, lang: str) -> str:
    lang = lang if lang in ["uz", "ru", "en"] else "uz"
    translated_type = CAR_TYPE_TRANSLATIONS[lang].get(car_type, car_type)
    
    st = (service_type or "").upper()
    if st == "1В":
        return "VIP"
    elif st in ["2Е", "2С"]:
        return "Ekonom" if lang != "ru" else "Эконом"
    
    return translated_type

def format_price_k(price_val: int) -> str:
    if price_val is None:
        return "—"
    if price_val >= 1000:
        return f"{price_val // 1000}k"
    return str(price_val)
